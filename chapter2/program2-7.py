import numpy as np     #numpyモジュールの導入

print(np.pi)           #円周率
print(np.sin(0))       #sin(0)
print(np.sin(np.pi/4)) #sin(pi/4)
print(np.sin(np.pi/3)) #sin(pi/3)
print(np.sin(np.pi/2)) #sin(pi/2)

print(np.cos(0))       #cos(0)
print(np.cos(np.pi/4)) #cos(pi/4)
print(np.cos(np.pi/3)) #cos(pi/3)
print(np.cos(np.pi/2)) #cos(pi/2)